public class Additional3 {
        public static void main(String[] args) {

            for (int i = 1; i <= 9; i++) {
                for (int k = 1; k <= 9; k++) {
                    int j;
                    j = i * k;
                    System.out.println(i + "*" + k + "=" + j);
                }
            }
        }
    }
